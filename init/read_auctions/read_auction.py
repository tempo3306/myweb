def get_ids(ids):
    idlist = ids.split()
    print(idlist)
    names = [idlist[i] for i in range(0, len(idlist), 2)]
    ID = [idlist[i] for i in range(1, len(idlist), 2)]
    return (names, ID)


def get_auction(auctions):
    a_list = auctions.split()
    alen = len(a_list)
    bid_number = [a_list[i] for i in range(0, alen, 3)]
    bid_password = [a_list[i] for i in range(1, alen, 3)]
    expired = [a_list[i] for i in range(2, alen, 3)]
    return (bid_number, bid_password, expired)


def excel_out(data):
    workbook = xlwt.Workbook(encoding='utf-8')
    # 创建一个worksheet
    worksheet = workbook.add_sheet('My Worksheet')

    # 写入excel
    # 参数对应 行, 列, 值
    names, ID, bid_number, bid_password, expired = data

    for i in range(len(names)):
        worksheet.write(i, 0, names[i])
        worksheet.write(i, 1, ID[i])
        worksheet.write(i, 2, bid_number[i])
        worksheet.write(i, 3, bid_password[i])
        worksheet.write(i, 4, expired[i])
    # 保存
    workbook.save('auction.xls')


def deal(ids, auctions):
    d1 = get_ids(ids)
    d2 = get_auction(auctions)
    data = d1 + d2
    excel_out(data)


